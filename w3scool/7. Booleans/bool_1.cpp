#include <iostream>
using namespace std;

// Booleans

// Boolean Expressions
// Anda dapat menggunakan operator perbandingan : untuk mengetahui apakah suatu ekspresi (atau variabel) benar atau salah.

// Contoh penulisan bool
// bool di tuliskan dalam () dia akan mengluarkan output 1 atau 0
// 1 = true
// 0 = false

int main()
{
    // Contoh Menggunakan Operator perbandingan
    int x = 10;
    int y = 9;
    cout << (x > y);

    cout << (10 > 9);
    cout << (10 == 15);
}
