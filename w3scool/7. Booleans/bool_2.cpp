int main()
{
    int myAge = 25;
    int votingAge = 18;

        // Contoh bool di gunakan dalam if dan else if : karena merekan cocok dan harus menggunakan bool 
    if (myAge >= votingAge) {
        cout << "Old enough to vote!";
    } else {
        cout << "Not old enough to vote.";
    }
}
