#include <iostream>
#include <cmath>
using namespace std;

// Math

// Contoh Math
// Max : max(x, y) = digunakan untuk menemukan nilai x dan y tertinggi
// Min : min(x,y) = digunakan untuk menemukan nilai x dan y terendah

// Contoh Cmath
// sqrt : (akar kuadrat)
// round : (membulatkan angka)
// log   : (logaritma natural)

int main()
{
    // Contoh Max
    cout << max(5, 10);

    // Contoh Min
    cout << min(5, 10);

    // Penggunaan Cmath
    cout << sqrt(64);
    cout << round(2.6);
    cout << log(2);
}
