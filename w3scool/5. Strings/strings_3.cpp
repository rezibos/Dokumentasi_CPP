// Strings Length

// Untuk mendapatkan panjang string gunakan fungsi : length() atau size()
// Keduanya sama sama buat nentuin panjang strings

int main()
{
    // Contoh Lenght
    string txt = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
    cout << "The length of the txt string is: " << txt.length();

    // Contoh Size
    string txt = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
    cout << "The length of the txt string is: " << txt.size();
}
