#include <iostream>
using namespace std;

// Materi Tentang New Lines

// \n = membuat line di bawah nya kayak << endl
// \t = membuat table horizontal
// \\ = menyisipkan karakter dengan garis miring
// \""\ = enyisipkan karakter tanda kutip ganda

int main()
{
    cout << "Helo dix \n ";
    cout << "Helo dix \t";
    cout << "Helo dix \\ halo ";
    cout << "Helo dix \"haloo\" ";
}
